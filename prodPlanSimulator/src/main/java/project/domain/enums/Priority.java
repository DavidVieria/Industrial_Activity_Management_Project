/**
 * Enum representing the priority levels that can be assigned to tasks, operations, or processes.
 * The priority levels range from {@code HIGH} to {@code LOW}.
 */
package project.domain.enums;

public enum Priority {
    /**
     * Represents a high-priority level, indicating tasks or operations that require immediate attention.
     */
    HIGH,

    /**
     * Represents a normal-priority level, indicating tasks or operations of standard importance.
     */
    NORMAL,

    /**
     * Represents a low-priority level, indicating tasks or operations of lesser urgency.
     */
    LOW
}
